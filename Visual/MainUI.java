    private int conversionNoeudEntier(char c) {
    	if (c >= 'A' &&  c <= 'H' ) {
    	return c-65;
    	}
		return -1;
    }
    
    private char conversionNoeudCaractere(int b) {
    	if (b >= 0 &&  b <= 7 ) {
    	return (char) (b+65);
    	}
		return 'Z';
    }
    
